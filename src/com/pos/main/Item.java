public class Item {
    
    private String barcode;
    private String name;
    private String unit;
    private float price;

    public String getBarcode() {
        return this.barcode;
    }

    public String getName() {
        return this.name;
    }

    public String getUnit() {
        return this.unit;
    }

    public float getPrice() {
        return this.price;
    }

}
